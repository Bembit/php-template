            A teljesítményt biztosító cookiekat az alábbiakra használjuk:

            web elemzések (Analytics): statisztikákat szolgáltat arról, hogy használják weboldalunkat.
            hirdetések válaszolási aránya: megmutatja, hogy milyen hatékonyak a hirdetéseink, beleszámítva azokat, amelyek a mi oldalunkra mutatnak.
            hiba menedzsment: segít nekünk, hogy fejlesszük a weboldalunkat a felmerülő hibák mérésével.
            design-ok tesztelése: weboldalunk különböző megjelenésű verzióinak teszteléséhez.
            A fenti cookiek közül néhányat harmadik fél használ, esetlegesen további információkért keresse fel ezen partnerek weboldalán található titoktartási leírásokat.

            A honlapunk használatával elfogadja a Teljesítményt biztosító cookie használatát. Ezen cookiek elfogadása feltétele honlapunk megfelelő használatának. Ha a felhasználó kikapcsolja ezeket a cookiekat, nem tudjuk garantálni weboldalunk megfelelő működését, illetve a megfelelő biztonságot oldalunk használata során. 
            </p>

