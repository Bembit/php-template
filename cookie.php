            <hr>
            <h4 class="text-center mb-5">Mi a cookie (süti)?</h4>
            
            <p class="lead text-left">
                A cookiek kis adatfájlok, amelyeket a böngésző helyez el a felhasználó számítógépén vagy eszközén. Többek között információt gyűjtenek, megjegyzik a látogató egyéni beállításait, és általánosságban megkönnyítik a weboldal használatát a felhasználók számára. A sütik önmagukban nem gyűjtik a számítógépen vagy a fájlokban tárolt adatokat. Olvassa el figyelmesen az alábbi tájékoztatót további információkért, hogyan gyűjtjük az információt, amikor Ön ezt a weboldalt használja.
            </p>
            <hr>
            <h4 class="text-center mb-5">A cookiek típusai</h4>
            <h5 class="text-center mb-5">Munkamenet cookie</h5>
            <p class="lead text-left">
                Ezek a cookiek ideiglenesen aktiválódnak, amíg a böngészés folyik. Azaz, attól a pillanattól, amikor a felhasználó megnyitja a böngésző ablakot, addig a pillanatig, amíg be nem zárja azt. Amint bezárul a böngésző, minden munkamenet cookie törlődik.
            </p>

            <h5 class="text-center mb-5">Állandó cookie</h5>
            <p class="lead text-left">
                Ezek a cookiek a felhasználó eszközén maradnak a cookie-ban meghatározott ideig. Minden alkalommal aktiválódnak, amikor a felhasználó meglátogatja a weboldalt.
            </p>

            <h5 class="text-center mb-5">Saját cookie</h5>
            <p class="lead text-left">
                Ezeket a cookiekat az a weboldal hozzá létre, amelyet a felhasználó egy bizonyos időben éppen meglátogat.
            </p>

            <h5 class="text-center mb-5">Harmadik fél cookie</h5>
            <p class="lead text-left">
                Amikor egy felhasználó meglátogat egy oldalt, egy másik fél aktiválja a cookie-t a honlapon keresztül.
            </p>
            <hr>

            <h4 class="text-center mb-5">Milyen sütiket használunk és miért?</h4>
            <h5 class="text-center mb-5">Feltétlenül szükséges cookie</h5>
            <p class="lead text-left">
            Ezek a cookiek segítséget nyújtanak honlapon történő mozgásban, megjegyzik a felhasználó egyes oldalakon végzett műveleteit. Ezen cookiek nélkül a szolgáltatások nem működnének. Ezek a cookiek nem tárolnak olyan adatokat a felhasználóról, hogy milyen oldalakat nézett meg az interneten, vagy amelyeket marketing célból lehetne felhasználni.

            A feltétlenül szükséges cookiekat az alábbiakra használjuk:

            emlékszik a begépelt információkra a megrendelések oldalain, amikor a felhasználó különböző oldalakra navigál a böngészés ideje alatt.
            emlékszik a megrendelt termékekre, szolgáltatásokra, miután kijelentkezik a weboldalról.
            felismeri a felhasználót, ha bejelentkezik a weboldalunkra.
            meggyőződik róla, hogy a felhasználó megfelelő szolgáltatáshoz kapcsolódik, miután néhány dolgot módosítottunk a weboldal működésén.
            elirányítja a felhasználót különböző szolgáltatások applikációihoz, vagy speciális szerverekhez.
            Ezen cookiek elfogadása feltétele honlapunk megfelelő használatának, így ha a felhasználó kikapcsolja ezeket a cookiekat, nem tudjuk garantálni weboldalunk megfelelő működését, illetve a megfelelő biztonságot oldalunk használata során.
            </p>
           
            <h5 class="text-center mb-5">Teljesítményt biztosító cookiek</h5>

